    @Override
    protected void doDML(int operation, TransactionEvent transactionEvent) {

        long days =
            daysBetween(getPeriodeProgTo().getValue(), getPeriodeProgFrom().getValue());

        if (operation == DML_INSERT) {
            Number runNumber;
            String userInitial = null;
            String userCreator = getCreatedBy();

            // Get date on MM/yy format
            java.util.Date date =
                new java.util.Date(System.currentTimeMillis());
            DateFormat dfMM = new SimpleDateFormat("MM");
            String currMon = dfMM.format(date);
            DateFormat dfYY = new SimpleDateFormat("yy");
            String currYear = dfYY.format(date);

            Number numMonth = new Number(Integer.parseInt(currMon));
            Number numYear = new Number(Integer.parseInt(currYear));

            // Get Run Number
            ViewObjectImpl runNumVo =
                (ViewObjectImpl)this.getDBTransaction().getRootApplicationModule().findViewObject("RunNumPropView1");
            runNumVo.setNamedWhereClauseParam("userName", userCreator);
            runNumVo.setNamedWhereClauseParam("runYear", numYear);
            runNumVo.setNamedWhereClauseParam("runMonth", numMonth);
            runNumVo.executeQuery();

            if (runNumVo.getAllRowsInRange().length > 0) {
                runNumProp = runNumVo.first();
                runNumber = (Number)runNumProp.getAttribute("Value");
                userInitial = (String)runNumProp.getAttribute("UserInitial");
                runNumProp.setAttribute("Value", runNumber.add(1));
            } else {
                // Get User Initial
                ViewObjectImpl userInitialVo =
                    (ViewObjectImpl)this.getDBTransaction().getRootApplicationModule().findViewObject("ProposalGetUserInitialView1");
                userInitialVo.setNamedWhereClauseParam("userName", userCreator);
                userInitialVo.executeQuery();
                
                if (userInitialVo.getAllRowsInRange().length > 0) {
                    Row userInitialRow = userInitialVo.first();
                    userInitial = (String)userInitialRow.getAttribute("UserInitial");                
                    Row newRow = runNumVo.createRow();
                    newRow.setAttribute("RunNumType", "PROP");
                    newRow.setAttribute("UserName", userCreator);
                    newRow.setAttribute("RunYear", currYear);
                    newRow.setAttribute("RunMonth", currMon);
                    newRow.setAttribute("Value", 2);
                    runNumVo.insertRow(newRow);
                    runNumber = new Number(1);
                } else { 
                    throw new JboException("User name \""+ userCreator +"\" tidak dikenali.");
                }
            }

            //Generate No Proposal
            String runNumberFormatted =
                String.format("%04d", runNumber.getBigDecimalValue().intValue());
            String numMonthFormatted =
                String.format("%02d", numMonth.getBigDecimalValue().intValue());
            String numYearFormatted =
                String.format("%02d", numYear.getBigDecimalValue().intValue());
            String noProp =
                userInitial + numYearFormatted + numMonthFormatted + runNumberFormatted;
            this.setProposalNo(noProp);
            this.setProgDays(new Number(days));
            
            // Set off invoice if biaya
            if (this.getDiscountType().equalsIgnoreCase("BIAYA")) {
                this.setMekanismePenagihan("OFFINVOICE");
            }
        }

        if (operation == DML_UPDATE) {
            // Set off invoice if biaya
            if (this.getDiscountType().equalsIgnoreCase("BIAYA")) {
                this.setMekanismePenagihan("OFFINVOICE");
            }
            this.setProgDays(new Number(days));
        }

        super.doDML(operation, transactionEvent);
    }

    private static long daysBetween(java.util.Date one, java.util.Date two) {
        long difference = (one.getTime() - two.getTime()) / 86400000;
        return Math.abs(difference);
    }
